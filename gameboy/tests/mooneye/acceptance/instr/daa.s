.include "common.s"

.define hram.sp_save_h $ff85
.define hram.sp_save_l $ff84
.define hram.testcase_h $ff83
.define hram.testcase_l $ff82
.define hram.a $ff81
.define hram.f $ff80

  ld hl, testcases1
  call run_tests
  ld hl, testcases2
  call run_tests

  test_ok

; Inputs
;   HL testcase pointer
run_tests:
  ld (hram.sp_save_l), sp
  ld sp, hram.f
  ld bc, $80*16
 
--
  ld a, h
  ldh (<hram.testcase_h), a
  ld a, l
  ldh (<hram.testcase_l), a
  ld a, (hl+)
  ldh (<hram.a), a
  ld a, (hl+)
  swap a
  ldh (<hram.f), a

  pop af
  daa
  push af

  ldh a, (<hram.a)
  cp (hl)
  jr nz, fail

  inc hl
  ldh a, (<hram.f)
  swap a
  cp (hl)
  jr nz, fail

  inc hl
  dec bc
  ld a, c
  or b
  jr nz, --

  ldh a, (<hram.sp_save_h)
  ld h, a
  ldh a, (<hram.sp_save_l)
  ld l, a
  ld sp, hl

  ret

fail:
  di
  ld sp, $fffe
  call disable_lcd_safe
  call reset_screen
  call print_load_font

  ldh a, (<hram.testcase_h)
  ld h, a
  ldh a, (<hram.testcase_l)
  ld l, a
  push hl

  ld hl, $9820
  print_string_literal "Test failed"

  call print_newline
  call print_newline
  print_string_literal "Input:"
  call print_newline

  print_string_literal "A: "
  pop bc
  ld a, (bc)
  inc bc
  push bc
  call print_hex8

  print_string_literal " F: "
  pop bc
  ld a, (bc)
  inc bc
  push bc
  call print_bin4

  call print_newline
  call print_newline
  print_string_literal "Expected output:"
  call print_newline

  print_string_literal "A: "
  pop bc
  ld a, (bc)
  inc bc
  push bc
  call print_hex8

  print_string_literal " F: "
  pop bc
  ld a, (bc)
  inc bc
  push bc
  call print_bin4

  call print_newline
  call print_newline
  print_string_literal "Actual output:"
  call print_newline

  print_string_literal "A: "
  ldh a, (<hram.a)
  call print_hex8

  print_string_literal " F: "
  ldh a, (<hram.f)
  swap a
  call print_bin4

  enable_lcd
  wait_vblank
  halt_execution

